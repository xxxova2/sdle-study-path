from __future__ import annotations

import json
import random
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
OUT_DIR = ROOT / "data" / "generated"
RNG = random.Random(42)  # reproducible shuffle of options


def pack(
    q: str,
    correct: str,
    wrongs: list[str],
    difficulty: str,
    explanation: str,
    topic: str,
    source: str,
    qid: str,
    subtopics: list[str] | None = None,
) -> dict:
    assert len(wrongs) == 3, f"{qid}: need exactly 3 wrongs"
    opts = [correct] + wrongs
    RNG.shuffle(opts)
    answer = opts.index(correct)
    return {
        "id": qid,
        "topic": topic,
        "difficulty": difficulty,
        "q": q,
        "options": opts,
        "answer": answer,
        "explanation": explanation,
        "source": source,
        "subtopics": subtopics or [],
    }


def validate(items: list[dict], expected_n: int, topic: str, source: str, id_prefix: str) -> None:
    assert len(items) == expected_n, f"{source}: expected {expected_n}, got {len(items)}"
    ids = []
    for i, it in enumerate(items, 1):
        assert it["topic"] == topic, it["id"]
        assert it["source"] == source, it["id"]
        assert it["difficulty"] in ("easy", "medium", "hard"), it["id"]
        assert len(it["options"]) == 4, it["id"]
        assert len(set(it["options"])) == 4, f"duplicate options: {it['id']}"
        assert 0 <= it["answer"] <= 3, it["id"]
        assert it["options"][it["answer"]], it["id"]
        assert it["q"] and it["explanation"], it["id"]
        assert it["id"].startswith(id_prefix), it["id"]
        ids.append(it["id"])
    assert len(ids) == len(set(ids)), f"duplicate ids in {source}"
    hist = Counter(it["answer"] for it in items)
    print(f"\n=== {source} ({topic}) n={len(items)} ===")
    print(f"  answer index histogram: {dict(sorted(hist.items()))}")
    print(f"  difficulty: {dict(Counter(it['difficulty'] for it in items))}")
    if any(it.get("subtopics") for it in items):
        st = Counter()
        for it in items:
            for s in it.get("subtopics") or []:
                st[s] += 1
        print(f"  subtopics: {dict(st)}")


